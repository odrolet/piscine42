#include<unistd.h>
#include<stdio.h>
#include<string.h>

int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	multiplicateur;

	i = 0;
	nb = 0;
	multiplicateur = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			multiplicateur = multiplicateur * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10;
		nb = nb + str[i] - '0';
		i++;
	}
	return (nb * multiplicateur);
}

int main()
{
	char	a[] = "    +-+----+354a488636";

	printf("%d", ft_atoi(a));
}
